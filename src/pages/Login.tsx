
import { useState } from "react";
import { useNavigate } from "react-router-dom";
import { Button } from "@/components/ui/button";
import { Input } from "@/components/ui/input";
import { Card, CardContent, CardHeader, CardTitle } from "@/components/ui/card";
import { toast } from "sonner";
import { supabase } from "@/integrations/supabase/client";

const SUPERVISOR_PASSWORD = "demo123"; // This should be moved to an environment variable

const Login = () => {
  const [email, setEmail] = useState("supervisor@example.com");
  const [password, setPassword] = useState("");
  const [isLoading, setIsLoading] = useState(false);
  const navigate = useNavigate();

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();
    setIsLoading(true);

    try {
      // Use the supervisor account for shared access, or let users enter their own credentials
      const { data, error } = await supabase.auth.signInWithPassword({
        email,
        password: password === SUPERVISOR_PASSWORD ? SUPERVISOR_PASSWORD : password,
      });

      if (error) throw error;

      toast("Welcome back!", {
        description: "Successfully logged in.",
      });

      navigate("/dashboard");
    } catch (error: any) {
      toast("Error", {
        description: error.message || "An error occurred during login.",
        variant: "destructive",
      });
    } finally {
      setIsLoading(false);
    }
  };

  return (
    <div className="min-h-screen flex items-center justify-center bg-gray-50 px-4">
      <Card className="w-full max-w-md">
        <CardHeader className="space-y-1">
          <CardTitle className="text-2xl font-bold text-center">
            Log In to OptimaFlow
          </CardTitle>
        </CardHeader>
        <CardContent>
          <form onSubmit={handleSubmit} className="space-y-4">
            <div className="space-y-2">
              <Input
                type="email"
                placeholder="Email"
                value={email}
                onChange={(e) => setEmail(e.target.value)}
                className="w-full"
              />
            </div>
            <div className="space-y-2">
              <Input
                type="password"
                placeholder="Password"
                value={password}
                onChange={(e) => setPassword(e.target.value)}
                className="w-full"
              />
            </div>
            <Button
              type="submit"
              className="w-full"
              disabled={isLoading}
            >
              {isLoading ? "Logging in..." : "Login"}
            </Button>
            <div className="text-center text-sm text-gray-500">
              <p>Supervisor login: Use "supervisor@example.com" with password "demo123"</p>
            </div>
          </form>
        </CardContent>
      </Card>
    </div>
  );
};

export default Login;
